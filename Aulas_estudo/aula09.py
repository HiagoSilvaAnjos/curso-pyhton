# Adição
sum = 10 + 10
print(sum)

# Subtração
subtraction = 20 - 10
print(subtraction)

# Multiplicação
multiplication = 10 * 10
print(multiplication)

# Divisão
division = 10 / 3
print(division)

# Divisão inteira que corta as casas decimais
division_int = 10 // 3
print(division_int)

# Potênciação
Potentiation = 10 ** 10
print(Potentiation)

# Resto da divisão (Módulo)
rest = 10 % 2
print(rest)